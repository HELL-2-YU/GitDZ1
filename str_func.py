def capitalize_words(input_string):
    """
    Делает заглавными первые буквы каждого слова в строке.

    Args:
        input_string (str): Строка, которую нужно преобразовать.

    Returns:
        str: Строка, где первые буквы каждого слова заглавные.
    """
    return input_string.title()


def to_uppercase(input_string):
    """
    Преобразует все символы строки в верхний регистр.

    Args:
        input_string (str): Строка, которую нужно преобразовать.

    Returns:
        str: Строка, содержащая все символы в верхнем регистре.
    """
    return input_string.upper()
